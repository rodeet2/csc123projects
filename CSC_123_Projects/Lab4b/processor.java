
public class processor {
	
	public String name;
	public String model;
	public String year;
	
	
	public processor(String n, String m, String y) {
		this.name = n;
		this.model = m;
		this.year = y;
	}


	public String getName() {
		return name;
	}


	public String getModel() {
		return model;
	}


	public String getYear() {
		return year;
	}



}
